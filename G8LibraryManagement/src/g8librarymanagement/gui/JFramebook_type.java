    private void but_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_deleteActionPerformed
        // TODO add your handling code here:
        int choose=JOptionPane.showConfirmDialog(null, "Do you want to delete?", "WARNING", JOptionPane.YES_NO_OPTION);
        if(choose==JOptionPane.YES_OPTION) {
            book_typeDAO btDAO=new book_typeDAO();
            ArrayList<book_type> list=btDAO.findALL();
            int index=jTableBT.getSelectedRow();
            btDAO.delete(list.get(index));
            loadData();
        }
    }//GEN-LAST:event_but_deleteActionPerformed

    private void but_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_saveActionPerformed
        // TODO add your handling code here:
        if(typeAction) {
            // add new
            book_type bt=new book_type();
            
            bt.setBook_type_name(txt_bt_name.getText());

            book_typeDAO btDAO=new book_typeDAO();
            
            String bookTname= txt_bt_name.getText();
            if(checkEmpty(bookTname)==1){
               JOptionPane.showMessageDialog(null, "Please input Book Type Name !");
            }
            else btDAO.addNew(bt);
            loadData();
        }
        else {
            // update
            book_type bt=new book_type();
            bt.setBook_type_id(Integer.valueOf(txt_bt_id.getText()));
            bt.setBook_type_name(txt_bt_name.getText());

            book_typeDAO btDAO=new book_typeDAO();
            
            String bookTname= txt_bt_name.getText();
            if(checkEmpty(bookTname)==1){
               JOptionPane.showMessageDialog(null, "Please input Book Type Name !");
            }
            else btDAO.update(bt);
            
            loadData();
            
        }
        updateStatus(true);
    }//GEN-LAST:event_but_saveActionPerformed

