    public void loadData() {
        tableModel = (DefaultTableModel) jTableBT.getModel();
        int numRow=tableModel.getRowCount();
        if (numRow>0) {
            for (int index=0; index<numRow; index++) {
                tableModel.removeRow(0);
            }
        }
        book_typeDAO tkdDAO=new book_typeDAO();       
        ArrayList<book_type> list=tkdDAO.findALL();
        int i=1;
        for (book_type bt : list) {
            tableModel.addRow(new Object[]{i++,bt.getBook_type_id(),bt.getBook_type_name()});
        }
    }
    public void loadResult(ArrayList<book_type> list) {
        tableModel = (DefaultTableModel) jTableBT.getModel();
        int numRow=tableModel.getRowCount();
        if (numRow>0) {
            for (int index=0; index<numRow; index++) {
                tableModel.removeRow(0);
            }
        }   
        int i=1;
        for (book_type bt : list) {
            tableModel.addRow(new Object[]{i++,bt.getBook_type_id(),bt.getBook_type_name()});
        }
    }
    public int checkEmpty(String input){
        int check = -1;
        if(input.isEmpty() || input.isBlank()){          
            check = 1;
        }
        return check;
    }

