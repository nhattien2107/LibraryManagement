    private void but_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_searchActionPerformed
        // TODO add your handling code here:
        if(rb_searchid.isSelected()) {
            int id=Integer.parseInt(txt_searchid.getText());
                borrowed_ticket_detailDAO tkdDAO=new borrowed_ticket_detailDAO();
                borrowed_ticket_detail rs=tkdDAO.findByID(id);
                if (rs==null) {
                    JOptionPane.showMessageDialog(null, "No result!!");
                } else {
                    ArrayList<borrowed_ticket_detail> list=new ArrayList<borrowed_ticket_detail>();
                    list.add(rs);
                    loadResult(list);
                }
        } else if(rb_searchticketID.isSelected()) {
            int id=Integer.parseInt(txt_searchticketID.getText());
            borrowed_ticket_detailDAO tkDAO=new borrowed_ticket_detailDAO();
            ArrayList<borrowed_ticket_detail> list=tkDAO.findByTicketID(id);
            if(list.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No Result!!");
            } else {
                loadResult(list);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please choose the method!!");
        }
        updateStatus(false);
    }//GEN-LAST:event_but_searchActionPerformed

