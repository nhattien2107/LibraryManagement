    private void but_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_saveActionPerformed
        // TODO add your handling code here:
        if(typeAction) {
            // add new
            borrowed_ticket_detail tkd=new borrowed_ticket_detail();

            try {
            tkd.setTicket_id(Integer.valueOf(txt_ticket_id.getText()));
            } catch (Exception ex){}
            try {
            tkd.setBook_id(Integer.valueOf(txt_book_id.getText()));
            } catch (Exception ex){}
            try {
            tkd.setAmount(Integer.valueOf(txt_amount.getText()));
            } catch (Exception ex){}
            
            borrowed_ticket_detailDAO tkdDAO=new borrowed_ticket_detailDAO();
            
            String ticketid= txt_ticket_id.getText();
            String bookid= txt_book_id.getText();
            String amount= txt_amount.getText();
            if(checkEmpty(ticketid)==1){
               JOptionPane.showMessageDialog(null, "Please input Ticket ID !");
            }
            else if (checkInt(ticketid)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Ticket ID !");
            }
            else if(checkEmpty(bookid)==1){
               JOptionPane.showMessageDialog(null, "Please input Book ID !");
            }
            else if (checkInt(bookid)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Book ID !");
            }
            else if(checkEmpty(amount)==1){
               JOptionPane.showMessageDialog(null, "Please input Amount !");
            }
            else if (checkInt(amount)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Amount !");
            }
            else tkdDAO.addNew(tkd);
            loadData();
        }
        else {
            // update
            borrowed_ticket_detail tkd=new borrowed_ticket_detail();
            tkd.setTicket_details_id(Integer.valueOf(txt_ticketD_id.getText()));
            
            try {
            tkd.setTicket_id(Integer.valueOf(txt_ticket_id.getText()));
            } catch (Exception ex){}
            try {
            tkd.setBook_id(Integer.valueOf(txt_book_id.getText()));
            } catch (Exception ex){}
            try {
            tkd.setAmount(Integer.valueOf(txt_amount.getText()));
            } catch (NumberFormatException ex){}
            
            borrowed_ticket_detailDAO tkdDAO=new borrowed_ticket_detailDAO();
            
            String ticketid= txt_ticket_id.getText();
            String bookid= txt_book_id.getText();
            String amount= txt_amount.getText();
            if(checkEmpty(ticketid)==1){
               JOptionPane.showMessageDialog(null, "Please input Ticket ID !");
            }
            else if (checkInt(ticketid)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Ticket ID !");
            }
            else if(checkEmpty(bookid)==1){
               JOptionPane.showMessageDialog(null, "Please input Book ID !");
            }
            else if (checkInt(bookid)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Book ID !");
            }
            else if(checkEmpty(amount)==1){
               JOptionPane.showMessageDialog(null, "Please input Amount !");
            }
            else if (checkInt(amount)==1){
                JOptionPane.showMessageDialog(null, "Please input valid Amount !");
            }
            else tkdDAO.update(tkd);
            loadData();
        }
        updateStatus(true);
    }//GEN-LAST:event_but_saveActionPerformed

