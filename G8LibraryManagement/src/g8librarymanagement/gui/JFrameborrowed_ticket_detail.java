    public void loadData() {
        tableModel = (DefaultTableModel) jTableTicketDetails.getModel();
        int numRow=tableModel.getRowCount();
        if (numRow>0) {
            for (int index=0; index<numRow; index++) {
                tableModel.removeRow(0);
            }
        }
        borrowed_ticket_detailDAO tkdDAO=new borrowed_ticket_detailDAO();       
        ArrayList<borrowed_ticket_detail> list=tkdDAO.findALL();
        int i=1;
        for (borrowed_ticket_detail tkd : list) {
            tableModel.addRow(new Object[]{i++,tkd.getTicket_details_id(),tkd.getTicket_id(),tkd.getBook_id(),tkd.getAmount()});
        }
    }
    public void loadResult(ArrayList<borrowed_ticket_detail> list) {
        tableModel = (DefaultTableModel) jTableTicketDetails.getModel();
        int numRow=tableModel.getRowCount();
        if (numRow>0) {
            for (int index=0; index<numRow; index++) {
                tableModel.removeRow(0);
            }
        }   
        int i=1;
        for (borrowed_ticket_detail tkd : list) {
            tableModel.addRow(new Object[]{i++,tkd.getTicket_details_id(),tkd.getTicket_id(),tkd.getBook_id(),tkd.getAmount()});
        }
    }
    
    public int checkEmpty(String input){
        int check = -1;
        if(input.isEmpty() || input.isBlank()){          
            check = 1;
        }
        return check;
    }
    public int checkInt(String input){
        int check = -1;
        try {
            Integer.parseInt(input);
        } catch(NumberFormatException ex){
            check = 1;}
        return check;
    }
            

